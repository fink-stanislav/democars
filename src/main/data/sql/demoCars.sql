	ID INTEGER GENERATED ALWAYS AS IDENTITY NOT NULL,
	MODEL INTEGER NOT NULL,
	PRICE DOUBLE,
	MILEAGE INTEGER,
	VIN VARCHAR(25),
	CONDITION VARCHAR(25),
	FEATURES INTEGER NOT NULL,
